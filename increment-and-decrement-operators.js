//Increment och Decrament Operator

var number = 0;
//postfix increment
console.log("postfix");
console.log(number++);
console.log(number);
console.log(number--);
console.log(number);

//prefix
var numberTwo = 0;
console.log("prefix");
console.log(++numberTwo);
console.log(--numberTwo);

console.log("for loop");
for (var i = 0; i <= 10; i++) {
  console.log(i);
}